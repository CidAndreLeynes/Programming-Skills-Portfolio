person = {
    'first_name': 'Cid',
    'last_name': 'Leynes',
    'age': 17,
    'city': 'Sharjah'
}

print(person['first_name'])  # Output: Cid
print(person['last_name'])  # Output: Leynes
print(person['age'])  # Output: 17
print(person['city'])  # Output: Sharjah

